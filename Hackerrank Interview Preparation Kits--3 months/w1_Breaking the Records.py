def breakingRecords(scores):
    # Write your code here
    maxx=scores[0]
    minx=scores[0]
    br_max=0
    br_min=0
    for i in range(1,len(scores)):
        if maxx < scores[i]:
            maxx = scores[i]
            br_max +=1
        if minx > scores[i]:
            minx = scores[i]
            br_min +=1
    return [br_max,br_min]

