def timeConversion(s):
    # Write your code here
    hr=int(s[:2])
    apm=s[-2:]
    rest=s[2:-2]
    if apm=='PM':
        if hr==12:
            return s[:-2]
        else:
            return str(hr+12)+rest
    elif apm=='AM':
        if hr==12:
            return '00'+rest
        else:
            return s[:-2]

