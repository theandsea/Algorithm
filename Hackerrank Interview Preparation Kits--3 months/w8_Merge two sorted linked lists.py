#
# SinglyLinkedListNode:
#     int data
#     SinglyLinkedListNode next
#
#
def mergeLists(head1, head2):
    # list ....separate read and write !!! to make it easier
    merge=[]
    a=head1
    b=head2
    while not (a is None or b is None):
        if a.data <= b.data:
            merge.append(a)
            a=a.next
        else:
            merge.append(b)
            b=b.next
    while not (a is None):
        merge.append(a)
        a=a.next
    while not (b is None):
        merge.append(b)
        b=b.next
            
    # linked
    for i in range(len(merge)-1):
        merge[i].next=merge[i+1]
    
    return merge[0]
    

