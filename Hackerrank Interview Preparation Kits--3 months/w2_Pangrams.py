def pangrams(s):
    # Write your code here
    slow=s.lower()
    dic={}
    for letter in slow:
        dic[letter] = True
    # check
    alph="abcdefghijklmnopqrstuvwxyz"
    for letter in alph:
        if letter not in dic:
            return "not pangram"
    return "pangram"

